module load gamess

THISNAME=product-PC1
MYRUNGAMESS=$HOME/CBE60547/Labs/Gamess/bin/rungms

cat <<EOF > ~/.gmsrc
set SCR=/scratch365/$USER
set USERSCR=$PWD
EOF

cat<<'EOF' > $THISNAME.inp
 $CONTRL SCFTYP=RHF DFTTYP=PBE RUNTYP=OPTIMIZE COORD=CART ISPHER=1 $END
 $BASIS GBASIS=PCseg-1 $END
 $DATA
H2ON2
C1 1
N 7 0.00000000 0.00000000 0.00000000
N 7 -1.21788342 -0.56515143 -0.01936524
O 8 -2.14322565 0.24080023 -0.00200729
H 1 -1.68759184 1.11459827 -0.49075552
H 1 0.80349912 -0.62947370 -0.01451607
 $END
EOF
$MYRUNGAMESS $THISNAME.inp > $THISNAME.out

grep 'NSERCH:' $THISNAME.out | gawk '{print $4, $7, $9}' > $THISNAME.sum

